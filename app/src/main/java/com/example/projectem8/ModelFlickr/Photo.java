package com.example.projectem8.ModelFlickr;

public class Photo {

    String id;
    String owner;
    String secret;
    String server;
    String title;

    public String getId() {
        return id;
    }

    public String getOwner() {
        return owner;
    }

    public String getSecret() {
        return secret;
    }

    public String getServer() {
        return server;
    }

    public String getTitle() {
        return title;
    }
}
